let listes = document.querySelectorAll("#photo-list li");
let total = document.querySelector("#total em")


/*************************************************************************************************/
/* ***************************************** FONCTIONS ***************************************** */
/*************************************************************************************************/
function photoSelected(){
    this.classList.toggle("selected");

    let selected = document.querySelectorAll(".selected");

    total.textContent = selected.length;
}


/*************************************************************************************************/
/* ************************************** CODE PRINCIPAL *************************************** */
/*************************************************************************************************/
listes.forEach(li => {
    li.addEventListener("click", photoSelected);
});

